<h2>Production et livraison</h2>
<p>
    Notre délai de production est en moyenne de 5 jours ouvrables. Le délai de livraison varie en fonction de votre région. Un numéro de suivi vous sera transmis par courriel, une fois votre commande expédiée. La compagnie de livraison peut changer selon votre région.
</p>

<p>
    Nous sommes fiers de vous proposer un tarif unique de livraison de <strong>14,95$</strong> (Taxes applicables en sus) pour tous les grands centres canadiens.
</p>

<p>
    Pour le Yukon, les Territoires du Nord-Ouest, le Nunavut et les régions éloignées, un prix vous sera fait sur mesure.
</p>
